package conquest;

/**
 * Represents owning player for different squares.
 */
public enum Player
{
    /**
     * Outside square
     */
    OUTSIDE,

    /**
     * Empty square
     */
    EMPTY,

    /**
     * Player 1 square
     */
    PLAYER1,

    /**
     * Player 2 square
     */
    PLAYER2
}
